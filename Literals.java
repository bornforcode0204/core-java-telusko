public class Literals {
    public static void main(String[] args) {
        
        int num1 = 34;

        int num2 = 0b101;
        int num3 = 0x7E;
        int num4 = 10_00_00_000;
        double num5 = 12e9; // 12 X 10^9

        System.out.println(num1);
        System.out.println(num2);
    }
}
